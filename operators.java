package first;

public class operators {

	public static void main(String[] args) {
		
		int a = 5;
		int b = 4;
		
		System.out.println(a+b);
		System.out.println(a-b);
		System.out.println(a*b);
		System.out.println(a/b);
		System.out.println(a%b);
		System.out.println(a++);
		System.out.println(++b);
		
		double x = 15.0;
		double y = 4.0;
		System.out.println(x/y);

		
	}

}
